CHIP_POWER_PATH=/sys/class/rfkill/rfkill0/state
LOG_TAG="bcm-fm"
LOG_NAME="${0}:"

loge ()
{
  /system/bin/log -t $LOG_TAG -p e "$LOG_NAME $@"
}

logi ()
{
  /system/bin/log -t $LOG_TAG -p i "$LOG_NAME $@"
}

failed ()
{
  loge "$1: exit code $2"
  exit $2
}

kill_power ()
{
  logi "kill_power"
  echo 0 > $CHIP_POWER_PATH
}


# init does SIGTERM on ctl.stop for service

kill_power

exit 0
